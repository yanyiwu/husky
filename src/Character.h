class CharacterConverter
{
    public:
        CharacterConverter(){ loadChineseTable(); };
        ~CharacterConverter() { m_hashChinese.clear(); }

        //\		
        bool loadChineseTable()
        {
            m_hashChinese.clear();
            int i=0;
            while(wordlist[i] && *wordlist[i])
            {
                m_hashChinese[ wordlist[i] ] = wordlist[i+1];
                i+=2;
            }
            return true;
        }

        //
        // 
        inline bool Convert_t2s(char* src)
        {
            if(!src || !*src) return true;
            if(m_hashChinese.size()==0) return false;

            char word[3]={0};
            while(*src)
            {
                if(*src<0) //
                {
                    if(!*(src+1))  // 
                    {
                        *src='\0';
                        return false;	
                    }
                    memcpy(word,src,2);
                    CHARACTER_TABLE::const_iterator it;
                    it=m_hashChinese.find(word);
                    if(it!=m_hashChinese.end())
                    {
                        strncpy(src,(*it).second.c_str(),2);
                    }
                    src++;
                }
                src++;
            }
            return true;
        }
        //
        //  
        // ->  a3b0 - a3b9
        inline bool ConvertDigitAlpha(char*src)
        {
            if(!src || !*src) return false;
            char * p=src;
            while(*src)
            {
                if(*src<0)
                {
                    if(*src=='\xa3')
                    {
                        src++;
                        if(*src>='\xb0' && *src<='\xb9')
                          *p++=*src-'\xb0' + '0';
                        else if(*src>='\xe1' && *src<='\xfa')
                          *p++=*src-'\xe1'+'a';
                        else if(*src>='\xc1' && *src<'\xda')
                          *p++=*src-'\xc1'+'A';
                        else
                        {
                            *p++=*(src-1);
                            *p++=*src;
                        }
                        src++;
                    }
                    else
                    {
                        *p++=*src++;
                        *p++=*src++;	
                    }

                }
                else
                  *p++=*src++;  //
            }
            *p='\0';
            return true;
        }
    public:
        CHARACTER_TABLE m_hashChinese;
};
